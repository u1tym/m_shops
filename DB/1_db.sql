-- ---------------------------------------------------------------------------
-- shops.shop_images
-- ---------------------------------------------------------------------------
CREATE TABLE shops.shop_images (
    id integer GENERATED BY DEFAULT AS IDENTITY,
    shop_id integer NOT NULL,
    aid integer NOT NULL,
    file_name character varying(255),
    mime_type character varying(100) NOT NULL,
    image_data bytea NOT NULL,
    file_size_bytes integer NOT NULL,
    sort_order integer NOT NULL DEFAULT 0,
    is_deleted boolean NOT NULL DEFAULT false,
    created_at timestamp without time zone NOT NULL DEFAULT now(),
    updated_at timestamp without time zone NOT NULL DEFAULT now(),
    CONSTRAINT shop_images_pkey PRIMARY KEY (id),
    CONSTRAINT shop_images_file_size_bytes_check
        CHECK (file_size_bytes > 0 AND file_size_bytes <= 5242880),
    CONSTRAINT shop_images_shop_aid_fkey FOREIGN KEY (shop_id, aid)
        REFERENCES shops.shops (id, aid) ON DELETE RESTRICT,
    CONSTRAINT shop_images_aid_fkey FOREIGN KEY (aid)
        REFERENCES public.accounts (id) ON DELETE RESTRICT
);

CREATE INDEX ix_shop_images_shop_id
    ON shops.shop_images (shop_id)
    WHERE is_deleted = false;
