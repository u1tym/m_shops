CREATE SCHEMA IF NOT EXISTS shops;

-- ---------------------------------------------------------------------------
-- shops.genres
-- ---------------------------------------------------------------------------
CREATE TABLE shops.genres (
    id integer GENERATED BY DEFAULT AS IDENTITY,
    aid integer NOT NULL,
    name character varying(100) NOT NULL,
    sort_order integer NOT NULL DEFAULT 0,
    is_deleted boolean NOT NULL DEFAULT false,
    created_at timestamp without time zone NOT NULL DEFAULT now(),
    updated_at timestamp without time zone NOT NULL DEFAULT now(),
    CONSTRAINT genres_pkey PRIMARY KEY (id),
    CONSTRAINT genres_aid_fkey FOREIGN KEY (aid)
        REFERENCES public.accounts (id) ON DELETE RESTRICT,
    CONSTRAINT genres_id_aid_key UNIQUE (id, aid)
);

CREATE INDEX ix_genres_aid ON shops.genres (aid);

CREATE UNIQUE INDEX uq_genres_aid_name_active
    ON shops.genres (aid, name)
    WHERE is_deleted = false;

-- ---------------------------------------------------------------------------
-- shops.shops
-- ---------------------------------------------------------------------------
CREATE TABLE shops.shops (
    id integer GENERATED BY DEFAULT AS IDENTITY,
    aid integer NOT NULL,
    name character varying(200) NOT NULL,
    address text,
    schedule_memo text,
    last_verified_on date,
    memo text,
    is_deleted boolean NOT NULL DEFAULT false,
    created_at timestamp without time zone NOT NULL DEFAULT now(),
    updated_at timestamp without time zone NOT NULL DEFAULT now(),
    CONSTRAINT shops_pkey PRIMARY KEY (id),
    CONSTRAINT shops_aid_fkey FOREIGN KEY (aid)
        REFERENCES public.accounts (id) ON DELETE RESTRICT,
    CONSTRAINT shops_id_aid_key UNIQUE (id, aid)
);

CREATE INDEX ix_shops_aid ON shops.shops (aid);

CREATE INDEX ix_shops_aid_name_active
    ON shops.shops (aid, name)
    WHERE is_deleted = false;

