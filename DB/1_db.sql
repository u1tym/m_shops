-- ---------------------------------------------------------------------------
-- shops.shop_opening_days
-- ---------------------------------------------------------------------------
CREATE TABLE shops.shop_opening_days (
    id integer GENERATED BY DEFAULT AS IDENTITY,
    shop_id integer NOT NULL,
    aid integer NOT NULL,
    day_of_week smallint NOT NULL,
